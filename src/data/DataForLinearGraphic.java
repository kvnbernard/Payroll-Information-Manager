package data;

public class DataForLinearGraphic {
	private int valuesX =0;
	private int ValuesY=0;
	public DataForLinearGraphic(int valuesX, int valuesY) {
		this.valuesX = valuesX;
		this.ValuesY = valuesY;
	}
	public int getValuesX() {
		return valuesX;
	}
	public int getValuesY() {
		return ValuesY;
	}
	public void setValuesX(int valuesX) {
		this.valuesX = valuesX;
	}
	public void setValuesY(int valuesY) {
		ValuesY = valuesY;
	}
	
	
}
